    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        String TglMasuk = dateFormat.format(tglmasuk.getDate());
        String TglKeluar = dateFormat.format(tglkeluar.getDate());
        
        String JenisKendaraan = "";
        if (motor.isSelected()) {
            JenisKendaraan = "MOTOR";
        } else {
            JenisKendaraan = "MOBIL";
        }
        
        String SQL ="INSERT INTO t_member (nm_petugas,no_resi,no_pol,jk,tgl_masuk,tgl_keluar,harga)"
                + "VALUES('"
                +petugas.getText()+"','"
                +noresi.getText()+"','"
                +nopol.getText()+"','"
                +JenisKendaraan+"','"
                +TglMasuk+"','"
                +TglKeluar+"','"
                +total.getText()+"')";
        int status = KoneksiDB.execute(SQL);
        if (status == 1) {
            JOptionPane.showMessageDialog(this, "Data berhasil diupdate","Sukses", JOptionPane.INFORMATION_MESSAGE);
            selectData();
        } else {
            JOptionPane.showMessageDialog(this, "Data gagal diupdate","Gagal", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

