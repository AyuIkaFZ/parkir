    private void tblDataMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblDataMouseClicked
        // TODO add your handling code here:
        int baris = tblData.getSelectedRow();
        if(baris != -1){
            noresi.setText(tblData.getValueAt(baris, 0).toString());
            petugas.setText(tblData.getValueAt(baris, 1).toString());
            nopol.setText(tblData.getValueAt(baris, 2).toString());
            if("MOTOR".equals(tblData.getValueAt(baris, 3).toString())){
                motor.setSelected(true);
            }else{
                mobil.setSelected(true);
            }
            SimpleDateFormat date = new SimpleDateFormat("yyyy-MM-dd");
            Date Masuk = null;
            try{
                Masuk = date.parse(tblData.getValueAt(baris, 4).toString());
            }catch (ParseException ex){
                Logger.getLogger(MainForm.class.getName()).log(Level.SEVERE, null, ex);
            }
            Date Keluar = null;
            try{
                Keluar = date.parse(tblData.getValueAt(baris, 5).toString());
            }catch(ParseException ex){
                Logger.getLogger(MainForm.class.getName()).log(Level.SEVERE, null, ex);
            }
            tglmasuk.setDate(Masuk);
            tglkeluar.setDate(Keluar);
        }
    }//GEN-LAST:event_tblDataMouseClicked

